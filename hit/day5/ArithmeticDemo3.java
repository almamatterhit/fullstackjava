package hit.day5;

public class ArithmeticDemo3 {
	public static void main(String[] args) {
		int i=10;
		
		//i=i+10;
		i+=10;
		System.out.println(i);//print 20
		i=10;
		i-=10;
		System.out.println(i);//print 0
		i=10;
		i*=10;
		System.out.println(i);//print 100
		
		i=10;
		i/=10;
		System.out.println(i);//print 1
		
		i=10;
		i%=10;
		System.out.println(i);//print 0
		
	}
}
